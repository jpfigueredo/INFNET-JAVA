    private void listarContatos(){
        
        lstContatos.removeAll();
        ids = new ArrayList<>();
        ArrayList<Contato> contatos = contatoRepository.listar();
        for (Contato contato : contatos) {
            lstContatos.add(contato.getNome());   
        }
    }
    
