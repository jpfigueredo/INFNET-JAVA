    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        
        Contato contato = new Contato();
        contato.setNome(txtNome.getText());
        contato.setEmail(txtEmail.getText());
        contato.setTelefone(txtTelefone.getText());
        
        if(idEmEdicao == -1){
            contatoRepository.inserirContato(contato);

        }else{
            contato.setId(idEmEdicao);
            idEmEdicao = -1;
            contatoRepository.alterarContato(contato);
        }
        listarContatos();
        limparDados();
        btnExcluir.setEnabled(false);
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void lstContatosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_lstContatosItemStateChanged
        
        int indice = lstContatos.getSelectedIndex();
        Contato contato = contatoRepository.obterContato(ids.get(indice));
        txtNome.setText(contato.getNome());
        txtEmail.setText(contato.getEmail());
        txtTelefone.setText(contato.getTelefone());
        idEmEdicao = contato.getId();
        btnExcluir.setEnabled(true);
        
    }//GEN-LAST:event_lstContatosItemStateChanged

